const escapeHtml = (value) =>
  value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");

const stripTags = (value) => value.replace(/<[^>]+>/g, "").replace(/\s+/g, " ").trim();

const getH1 = (html) => stripTags((html.match(/<h1[^>]*>[\s\S]*?<\/h1>/) || [""])[0]);
const getLead = (html) => stripTags((html.match(/<p class="lead">[\s\S]*?<\/p>/) || [""])[0]);

